export function isHomeKey(event: KeyboardEvent) {
  return event.key === 'Home';
}

export function isEndKey(event: KeyboardEvent) {
  return event.key === 'End';
}

export function isUpArrow(event: KeyboardEvent) {
  return event.key === 'ArrowUp';
}

export function isDownArrow(event: KeyboardEvent) {
  return event.key === 'ArrowDown';
}

export function isRightArrow(event: KeyboardEvent) {
  return event.key === 'ArrowRight';
}

export function isLeftArrow(event: KeyboardEvent) {
  return event.key === 'ArrowLeft';
}

export function isEnter(event: KeyboardEvent) {
  return event.key === 'Enter';
}

export function isEscape(event: KeyboardEvent) {
  return event.key === 'Escape';
}

export function isSpaceKey(event: KeyboardEvent) {
  return event.key === ' ';
}
